// Given a 2D board and a word, find if the word exists in the grid.
//
// The word can be constructed from letters of sequentially adjacent cell, where "adjacent" cells
// are those horizontally or vertically neighboring. The same letter cell may not be used more than once.

// board = [
//   ['A','B','C','E'],
//   ['S','F','C','S'],
//   ['A','D','E','E']
// ]
//
// Given word = "ABCCED", return true.
// Given word = "SEE", return true.
// Given word = "ABCB", return false.

const exist = (board, word) => {
  for (let i = 0; i < board.length; i++)
    for (let j = 0; j < board[i].length; j++)
      if (board[i][j] === word[0] && backtracking(board, word, j, i))
        return true;
  return false;
};

const backtracking = (board, word, x, y, visited = {}) => {
  if (word.length === 0) return true;

  if (
    y < 0 || y >= board.length ||
    x < 0 || x >= board[y].length ||
    (visited[y] && visited[y][x]) ||
    board[y][x] !== word[0]
  ) return false;

  visited[y] = visited[y] || {};
  visited[y][x] = 1;

  if (
    backtracking(board, word.slice(1), x - 1, y, visited) ||
    backtracking(board, word.slice(1), x, y - 1, visited) ||
    backtracking(board, word.slice(1), x + 1, y, visited) ||
    backtracking(board, word.slice(1), x, y + 1, visited)
  ) return true;

  visited[y][x] = 0;
};

